/**
 * @param {string[]} words
 * @param {string} chars
 * @return {number}
 */
var countCharacters = function(words, chars) {
  let word = '';
  let res;
  words.forEach((el, index) => {
    res = detectGoodWords(el, el.length - 1, chars);
    if (!res || res === undefined) return;
    word += res;
  });
  return word.length;
};

/**
 * 
 * @param {string} word 
 * @param {number} post 
 * @param {string} chars 
 */
var detectGoodWords = function(word, post, chars) {
  if (!chars.includes(word[post])) {
     return; 
  }
  if (post === 0) {
      return word;
  }
  chars = chars.replace(word[post], '');
  return detectGoodWords(word, post-1, chars);
}